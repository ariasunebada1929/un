CREATE TABLE PARSONAL_TRN
(STAFF_ID VARCHAR(5) NOT NULL COMMENT '社員ID',
SECTION_CD VARCHAR(2) NOT NULL COMMENT '課コード',
UNIT_CD VARCHAR(2) COMMENT 'ユニットコード',
DETAIL VARCHAR(100) COMMENT '詳細',
BASIC_WORK_START CHAR(4) NOT NULL COMMENT '基本勤務時間(出社)',
BASIC_WORK_END CHAR(4) NOT NULL COMMENT '基本勤務時間(退社)',
ZANGYO_ADJUST VARCHAR(4) COMMENT '残業調整',
WORK_CLOSE_DAY CHAR(6) COMMENT '勤怠締め済み日付',
PRIMARY KEY(STAFF_ID)
)
COMMENT='個人設定トラン';