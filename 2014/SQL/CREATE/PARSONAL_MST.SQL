CREATE TABLE PERSONAL_MST
(STAFF_ID VARCHAR(5) NOT NULL COMMENT '社員ID',
FIRST_NAME VARCHAR(20) NOT NULL COMMENT '姓',
LAST_NAME VARCHAR(20) NOT NULL COMMENT '名',
KANA_FIRST VARCHAR(20) NOT NULL COMMENT 'フリガナ姓',
KANA_LAST VARCHAR(20) NOT NULL COMMENT 'フリガナ名',
SEX VARCHAR(1) COMMENT '性別',
VALID_FLAG INT(1) NOT NULL COMMENT '有効フラグ',
PRIMARY KEY(STAFF_ID)
)
COMMENT='個人マスタ';