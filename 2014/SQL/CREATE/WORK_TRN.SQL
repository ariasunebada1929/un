CREATE TABLE WORK_TRN
(STAFF_ID VARCHAR(5) NOT NULL COMMENT '社員ID',
YEAR VARCHAR(4) NOT NULL COMMENT '年',
MONTH VARCHAR(2) COMMENT '月',
DAY VARCHAR(2) NOT NULL COMMENT '日',
SUBMIT_REQUEST_1_CD CHAR(2) COMMENT '休暇',
SUBMIT_REQUEST_2_CD CHAR(2) COMMENT '休出/振代休',
SUBMIT_REQUEST_3_CD CHAR(2) COMMENT '通院/電遅',
SUBMIT_REQUEST_4_CD CHAR(2) COMMENT 'A/B変',
COMP_HOLIDAY VARCHAR(4) COMMENT '振替日',
ACTUAL_WORK_START VARCHAR(4) NOT NULL COMMENT '実績勤務時間（出社）',
ACTUAL_WORK_END VARCHAR(4) NOT NULL COMMENT '実績勤務時間（退社）',
RESTHOURS VARCHAR(4) NOT NULL COMMENT '休憩',
PRIMARY KEY(STAFF_ID)
)
COMMENT='勤怠トラン';