CREATE TABLE WORK_TRN
(STAFF_ID VARCHAR(5) NOT NULL COMMENT '社員ID',
YEAR VARCHAR(4) NOT NULL COMMENT '年',
MONTH VARCHAR(2) NOT NULL COMMENT '月',
DAY VARCHAR(2) NOT NULL COMMENT '日',
SUBMIT_REQUEST_1_CD CHAR(2) COMMENT '休暇',
SUBMIT_REQUEST_2_CD CHAR(2) COMMENT '休出/振代休',
SUBMIT_REQUEST_3_CD CHAR(2) COMMENT '通院/電遅',
SUBMIT_REQUEST_4_CD CHAR(2) COMMENT 'A/B変',
COMP_HOLIDAY VARCHAR(4) COMMENT '振替日',
DETAIL VARCHAR(100) COMMENT '詳細',
BASIC_WORK_START CHAR(4) NOT NULL COMMENT '基本勤務時間(出社)',
BASIC_WORK_END CHAR(4) NOT NULL COMMENT '基本勤務時間(退社)',
ACTUAL_WORK_START CHAR(4) NOT NULL COMMENT '実績勤務時間（出社）',
ACTUAL_WORK_END CHAR(4) NOT NULL COMMENT '実績勤務時間（退社）',
RESTHOURS CHAR(4) NOT NULL COMMENT '休憩',
ZANGYO_ADJUST CHAR(4) COMMENT '残業調整',
PRIMARY KEY(STAFF_ID)
)
COMMENT='勤怠トラン';